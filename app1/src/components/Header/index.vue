          <a href="###">商家后台</a>
        </div>
      </div>
    </div>
    <!--头部第二行 搜索区域-->
    <div class="bottom">
      <h1 class="logoArea">
        <router-link class="logo" title="尚品汇" to="/home">
          <img src="./images/logo.png" alt="" />
        </router-link>
      </h1>
      <div class="searchArea">
        <form action="###" class="searchForm">
          <input
            type="text"
            id="autocomplete"
            class="input-error input-xxlarge"
            v-model="keyword"
          />
          <button
            class="sui-btn btn-xlarge btn-danger"
            type="button"
            @click="goSearch"
          >
            搜索
          </button>
        </form>
      </div>
    </div>
  </header>
</template>

<script>
export default {
  data() {
    return {
      keyword: "",
    };
  },
  methods: {
    // 搜索按钮，跳转到search路由组件
    goSearch() {
      // 字符串写法
      // this.$router.push('/search/'+this.keyWord+"?k="+this.keyWord)
      // 模板字符串写法
      // this.$router.push(`/search/${this.keyWord}?k=${this.keyWord}`)
      // 对象写法

      //验证Vue-Router引入Promise技术,最笨的方法,给push传递第二个、第三个参数【回调函数】
      //下面这种写法：治标不治本！！！！
      // let result = this.$router.push({name: "search",params: { keyword: this.keyword|| undefined}},()=>{},()=>{});
      if (this.$route.query) {
        let location = {
          name: "search",
          params: { keyword: this.keyword || undefined },
        };
        location.query = this.$route.query;
        this.$router.push(location);
      }
    },
    // 退出登录
    async logout() {
      try {
        // 1.需要发送请求，通知服务器退出登录【清除一些数据，token】
        // 2.清除项目当中的数据【userInfo，token】
        await this.$store.dispatch("user/userLogout");
        this.$router.push({
            path:'/home'
        })
      } catch (error) {}
    },
  },
  mounted() {
    // 通过全局事件总线清除关键字
    this.$bus.$on("clear", () => {
      this.keyword = "";
    });
  },
  computed: {
    // 用户名信息
    userName() {
      return this.$store.state.user.userInfo.loginName;
    },
  },
};
</script>

